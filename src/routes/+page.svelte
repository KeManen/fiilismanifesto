<h1>Manifesto for Fiilis Software Development</h1>
<br><br><br>
<p>We are uncovering better fiilis of developing software by doing it and helping others do it. Through this work we have come to value:</p>
<br><br>

<p><b>Good Fiilis</b> over Individuals and interactions</p>
<p><b>Good Fiilis</b> over Working Product</p>
<p><b>Good Fiilis</b> over Customer Collaboration</p>
<p><b>Good Fiilis</b> over Responding to change</p>
<br><br>

<p>That is, while there is value in the items on the right, we value the Fiilis on the left more.</p>
